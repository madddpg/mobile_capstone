import 'package:iconstruct/features/auth/presentation/widgets/otp_dialog.dart';

class RegisterScreen extends StatefulWidget {
  const RegisterScreen({super.key});

  @override
  State<RegisterScreen> createState() => _RegisterScreenState();
}

class _RegisterScreenState extends State<RegisterScreen> {
  final _firstNameController = TextEditingController();
  final _lastNameController = TextEditingController();
  final _emailController = TextEditingController();
  final _passwordController = TextEditingController();
  final _confirmPasswordController = TextEditingController();
  final ScrollController _scrollController = ScrollController();
  final EmailService _emailService = EmailService();

  bool _loading = false;
  bool _obscurePassword = true;
  bool _obscureConfirmPassword = true;
  bool _acceptedTerms = false;

  String? _emailError;
  String? _passwordError;
  String? _confirmPasswordError;

  final List<Map<String, String>> _termsAndConditions = const [
    {
      'title': '1. Purpose of iConstruct',
      'body':
          'iConstruct is a smart construction support system designed to help users plan basic residential construction projects through material estimation, cost estimation, project saving, supplier recommendations, and quotation support. The system is intended as a planning tool and should not replace professional construction, engineering, or architectural advice.',
    },
    {
      'title': '2. Accuracy of Material and Cost Estimates',
      'body':
          'All material quantities and cost estimates generated by iConstruct are for reference only. Actual construction costs may vary depending on supplier prices, labor costs, delivery fees, product availability, site conditions, and market changes. Users must verify estimates before making purchases or starting construction work.',
    },
    {
      'title': '3. User Account Responsibility',
      'body':
          'Users are responsible for providing accurate registration information and keeping their login credentials secure. Any action performed using a user’s account, such as saving projects, posting project requests, or requesting quotations, may be considered the responsibility of the account owner.',
    },
